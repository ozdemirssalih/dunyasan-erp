const lines = rawData.trim().split('\n');
const sqlValues = [];

lines.forEach((line, index) => {
  if (!line.trim()) return;

  // Tab veya multiple space ile ayrılmış
  const parts = line.split(/\s{2,}|\t/).map(p => p.trim()).filter(Boolean);

  if (parts.length < 4) return; // En az 4 alan olmalı

  const toolCode = parts[0] || '';
  const toolName = parts[1] || '';
  const toolType = parts[2] || '';
  const quantity = parseInt(parts[3]) || 0;
  const location = parts[4] || '';
  const brand = parts[5] || '';

  // min_quantity belirleme
  const minQty = toolType.includes('ELMAS INSERT UÇ') ? 10 : 5;

  // SQL escape
  const escapeSql = (str) => str.replace(/'/g, "''");

  // NULL veya string
  const brandValue = brand ? `'${escapeSql(brand)}'` : 'NULL';

  sqlValues.push(
    `  ('${escapeSql(toolCode)}', '${escapeSql(toolName)}', '${escapeSql(toolType)}', ${quantity}, ${minQty}, '${escapeSql(location)}', 'available', ${brandValue})`
  );
});

